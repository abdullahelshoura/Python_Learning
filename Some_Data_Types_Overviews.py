# All Data in Python are Object
# -----------------------------
# type() show the value type (int,float,string,boolean,....)
print(type(10))
print(type(10.9))
print(type("Hello Python"))
print(type([1, 2, 3, 4]))  # list []
print(type((1, 2, 3, 4)))  # Tuple ()
print(type({"one": 1, "two": 2, "three": 3}))  # Dictionary {"key",value}
print(type(2 == 4))  # 2==4 checks the condition and returnts boolean (True , False)
