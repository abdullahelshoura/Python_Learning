# Name Convention and Rules
# [1] Can Start with (a-z A-Z) Or Underscore
# [2] Cannot Start with Number or Special Character
# [3] Can include [0-9] or underscore
# [4] cannot include Special Characters
# [5] Name is Not like name (Case Sesitive)
# Syntax => [Variable Name] [Assignment Operatot] [Value]

