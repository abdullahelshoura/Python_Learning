# Escape Sequences Characters
# ----------------------------------------------------------

# \b will delete the space between Hello word and World word ( Output : HelloWorld )
print("Hello \bWorld")

# \ : Escape New Line
print("Hello \
Iam \
Here")

# \\ : Escape Backslash to be printed
print("I Love Back Slash \\")

# \' : Escape Single Quotes to be printed
print('I Love Single Qutes \'Test\'')

# \" : Escape Double Quotes to be printed
print("I Love Double Qutes \"Test\"")

# \n : new line
print("I Love Double Qutes\n \"Test\"")

# \r : Carriage Return : Relpaces the number of chars equal to the word after it of word before it with word after it .
print("123456789\rABDULLAH")

# \t : Horizontal Tap
print("123456789\tABDULLAH")

# \xHEX : character Hex Value
print("\x4F\x73\x4F\x73")
